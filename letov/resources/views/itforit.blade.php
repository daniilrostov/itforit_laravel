function valid(form) {
        var fail = false;
        var name = form.name.value;
        var addr = form.email.value;
        var comm = form.comments.value;
        var name_pattern = /[а-яА-Я]/i;
        var addr_pattern = /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/i;
        if (name == "" || name == " ")
        fail = "Не задано Имя!";
                else if ( name.search(name_pattern) )
                        fail = "Введите Имя русскими буквами!";
                else if ( name.length > 15 )
                        fail = "Для ввода имени используйте менне 15 знаков!";
                else if (addr == "" || addr == " ")
                        fail = "Не задан получатель услуг!";
                else if (addr.search(addr_pattern))
                        fail = "Введите почту корректно!";
                else if ( addr.length > 30 )
                        fail = "Для адресата используйте менне 30 знаков!";
                else if (comm == "" || comm == " ")
                        fail = "Введите коментарий!";
                else if ( comm.length > 100 )
                        fail = "Очень большой текст";
                if(fail) {
                        var element = document.getElementById("text")
                        element.style.color = "red";
                        element.innerHTML = fail;
                        return;
                        }
                else
                result = confirm("Отправляю от: "+name);
                if ( result ) {
                    fromsql("mail.php?name="+name+"&addr="+addr+"&comm="+comm, mail);
                    clearall();
                } else {console.log("stop of user")}
}
